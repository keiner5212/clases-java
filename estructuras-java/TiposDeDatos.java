public class TiposDeDatos {
    public static void main(String[] args) {
        // Tipos primitivos
        int x = 5; // Entero de 32 bits
        long y = 123456789L; // Entero largo de 64 bits
        float z = 3.14f; // Número de coma flotante de 32 bits
        double w = 3.14; // Número de coma flotante de 64 bits
        char c = 'A'; // Carácter Unicode de 16 bits
        boolean b = true; // Valor booleano (verdadero o falso)

        // Tipos de referencia
        String s = "Hola"; // Cadena de caracteres
        int[] a = {1, 2, 3}; // Arreglo de enteros
    }
}
