public class ImpresionYEscape {
    public static void main(String[] args) {
        // Imprimir texto sin salto de línea al final
        System.out.print("Hola, ");
        System.out.print("mundo!");
        System.out.println(); // Agregar un salto de línea

        // Imprimir texto con salto de línea al final
        System.out.println("Hola, mundo!");

        // Caracteres de escape comunes en Java
        System.out.println("Salto de línea: \\n");
        System.out.println("Tabulador: \\t");
        System.out.println("Barra invertida: \\\\");
        System.out.println("Comillas dobles: \\\"");
    }
}
