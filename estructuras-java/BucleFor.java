public class BucleFor {
    public static void main(String[] args) {
        // Ejemplo de la sentencia for
        // Inicialización: int i = 0
        // Condición: i < 5
        // Incremento: i++
        for (int i = 0; i < 5; i++) {
            System.out.println(i); // Imprime los números del 0 al 4
        }
    }
}
